	int i=0, i1 =  3;
	int *c = &i;
	//int *c = i; //报错,因为在初始化指针时,不能将整型变量直接赋给指针所指的对象;
	
	//c = i1; //报错,因为在任何时候都不能将整形变量直接赋给指针;
